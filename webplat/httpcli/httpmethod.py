
HTTP_METHOD_GET = "GET"  # 方法请求一个指定资源的表示形式，使用GET的请求应该只被用于获取数据。
HTTP_METHOD_HEAD = "HEAD"  # 方法请求一个与GET请求的响应相同的响应，但没有响应体。
HTTP_METHOD_POST = "POST"  # 方法用于将实体提交到指定的资源，通常导致在服务器上的状态变化或副作用。
HTTP_METHOD_PUT = "PUT"  # 方法用请求有效载荷替换目标资源的所有当前表示。
HTTP_METHOD_DELETE = "DELETE"  # 方法删除指定的资源。
HTTP_METHOD_CONNECT = "CONNECT"  # 方法建立一个到由目标资源标识的服务器的隧道。
HTTP_METHOD_OPTIONS = "OPTIONS"  # 方法用于描述目标资源的通信选项。
HTTP_METHOD_TRACE = "TRACE"  # 方法沿着到目标资源的路径执行一个消息环回测试。
HTTP_METHOD_PATCH = "PATCH"  # 方法用于对资源应用部分修改。
